// Function to add two numbers
function addNumbers(num1, num2) {
    return num1 + num2;
}

// Using the function
const number1 = 5;
const number2 = 10;
const result = addNumbers(number1, number2);

// Logging the result
console.log(`The sum of ${number1} and ${number2} is ${result}.`);
